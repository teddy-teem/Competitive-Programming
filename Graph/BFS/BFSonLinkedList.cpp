struct TreeNode {
    int val;
    TreeNode* left;
    TreeNode* right;

    TreeNode(int x) : val(x), left(nullptr), right(nullptr) {}
};


void bfs(TreeNode* root)
{
    queue<TreeNode*> q;
    q.push(root);
    while (!q.empty()) {
        TreeNode* front = q.front();
        cout<<front->val<<" ";
        q.pop();

        if(front->left != NULL) {
            q.push(front->left);
        }
        if(front->right != NULL) {
            q.push(front->right);
        }
    }
}

